import { cn } from "@/lib/utils"
import { HugeiconsIcon } from "@hugeicons/react"
import { Loading03Icon } from "@hugeicons/core-free-icons"

function Spinner({ className, strokeWidth, ...props }: React.ComponentProps<"svg"> & { strokeWidth?: number | string }) {
  const normalizedStrokeWidth = typeof strokeWidth === "string" ? Number(strokeWidth) : strokeWidth

  return (
    <HugeiconsIcon
      icon={Loading03Icon}
      strokeWidth={normalizedStrokeWidth ?? 2}
      role="status"
      aria-label="Loading"
      className={cn("size-4 animate-spin", className)}
      {...props}
    />
  )
}

export { Spinner }
